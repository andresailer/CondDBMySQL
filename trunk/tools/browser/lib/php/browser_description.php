<p class='main'> Please fill the form available from the left side menu (at the bottom) to change Database if you prefer to use other than the default one!</p>
<p class='main'>A folder tree will be available on the left side. You can click both on folders or folder sets to see their description. </p>
<p class='main'>If a folder is selected a menu will be available with options regarding to that folder type, please use it to select the type of "Search" that you want to perform. The options available are:
<ul><li><b>Browse Tag </b> - Selecting this option will be able to Search in a specific <i>Tag</i>. <br>
        This option it's available in Folders of type BLOB and CondDBTable with versions.
    <li><b>Browse at Point </b> - With this option you'll be able to search objects that are valid for a specific point. <br>         This option it's available in Folders of type BLOB and CondDBTable with versions.
    <li><b>Browse History </b> - This option allows to Search objects that are valid from a <i>Since</i> to a <i>Till</i> tim
e.<br>
        This option it's available in Folders of type BLOB, CondDBTable (with and without versions) and CondDBTable with Id.
</ul>

<p class='main'>Note that some options may be disabled on the menu depending on the folder type. Some folders do not support all searching op
tions.</p>

